When you enroll in Ion, you are sharing personal information. Keeping that
information safe is important to us and to the integrity of Ion. Here is how we
safeguard your data and protect your identity:
<ul style="height: 300px; overflow: auto;">
  <li>
    Ion will not collect your data before you enroll in the program. Once you
    enroll, we will not collect new kinds of data without telling you and
    getting your permission.
  </li>
  <li>
    Your data is encrypted before it is sent from your browser and is not
    decrypted until it is in a secure datastore that is not connected to the
    internet.
  </li>
  <li>
    Your data will be linked to a randomly generated ID we only use for Ion. It
    is used to keep each personâ€™s data together on the server and helps us
    improve our analysis.
  </li>
  <li>Ion does not collect data from Private Browsing sessions.</li>
  <li>
    Ion and Ion studies do not collect data from Private Browsing sessions
    without your permission.
  </li>
  <li>
    All proposed studies will be reviewed by a panel consisting of privacy
    experts, internet researchers, and security engineers to ensure that the
    data being collected is appropriate and in accordance with our lean data
    practices.
  </li>
  <li>
    Pioneer will only share personal browsing data within our own secure
    environment. Mozilla will always control access to the data and can always
    limit that access.
  </li>
  <li>
    Mozilla will explore the collected data from Pioneer users to understand
    internet health, user behavioral trends, and issues of public concern. We
    will publish findings that benefit the public interest. We will only publish
    findings at the level of trends. We will not publish anything that reveals
    the behavior of individual users.
  </li>
  <li>
    In the future, Mozilla may pursue commercial opportunities that advance our
    mission or create more opportunities for competition on the internet using
    aggregated, non-personally identifiable data collected by the Pioneer
    program. Mozilla will disclose our commercial intent to you ahead of data
    collection. Mozilla will never sell your personally identifiable
    information.
  </li>
</ul>
